package model;

public class TableItem {

    private String id;
    private String codigo;
    private String description;
    private String line;
    private String peso;

    public TableItem(String id, String codigo, String description, String line, String peso) {
        this.id = id;
        this.codigo = codigo;
        this.description = description;
        this.line = line;
        this.peso = peso;
    }

}
